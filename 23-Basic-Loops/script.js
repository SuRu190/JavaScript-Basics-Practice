// Print numbers from 1 to 5 using for, while, and do-while loops.

// using for loop
for (let i = 1; i <= 5; i++) {
    console.log(i);
}

// using while loop
let j = 1;
while (j <= 5) {
    console.log(j);
    j++;
}

// using do-while loop
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 5);
